package asteroids.TwoD;

public class TwoDPoint {

    private final float _x;
    private final float _y;
    
    public TwoDPoint(float x, float y) {
        _x = x;
        _y = y;
    }

    public float getX() {
        return _x;
    }

    public float getY() {
        return _y;
    }
    
}
